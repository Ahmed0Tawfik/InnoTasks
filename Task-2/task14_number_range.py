print("Task 14: Number Range")
num = 25
if 0 <= num <= 10:
    print(f"{num} belongs to Range 1: 0 to 10")
elif 11 <= num <= 20:
    print(f"{num} belongs to Range 2: 11 to 20")
elif 21 <= num <= 30:
    print(f"{num} belongs to Range 3: 21 to 30")
elif 31 <= num <= 40:
    print(f"{num} belongs to Range 4: 31 to 40")
else:
    print(f"{num} is outside the specified ranges") 